def convert_json_to_ids_xml(
    json_data: Dict[str, Any],
    xsd_path: Optional[str] = None,
    **kwargs
) -> str:
    """
    将 JSON 数据转换为 IDS XML 字符串

    Args:
        json_data: Pipeline 输出的 JSON 字典
        xsd_path: 可选的 XSD 文件路径
        **kwargs: 传递给 IDSToXMLConverter.convert 的额外参数
            - title: 标题
            - copyright: 版权信息
            - version: 版本号
            - author: 作者邮箱
            - date_str: 日期字符串

    Returns:
        格式化的 IDS XML 字符串

    Raises:
        XSDValidationError: XSD 校验失败
        IDSConversionError: 其他转换错误

    Example:
        >>> json_data = {"specifications": [...]}
        >>> xml_str = convert_json_to_ids_xml(json_data)
        >>> print(xml_str)
    """
    converter = IDSToXMLConverter(xsd_path=xsd_path)
    return converter.convert(json_data, **kwargs)


def convert_json_file_to_ids_xml(
    json_path: str,
    output_path: Optional[str] = None,
    xsd_path: Optional[str] = None,
    **kwargs
) -> str:
    """
    将 JSON 文件转换为 IDS XML 文件

    Args:
        json_path: 输入 JSON 文件路径
        output_path: 输出 XML 文件路径（可选，不提供则返回字符串）
        xsd_path: 可选的 XSD 文件路径
        **kwargs: 传递给 convert_json_to_ids_xml 的额外参数

    Returns:
        格式化的 IDS XML 字符串

    Raises:
        FileNotFoundError: JSON 文件不存在
        XSDValidationError: XSD 校验失败
        IDSConversionError: 其他转换错误
    """
    # 读取 JSON 文件
    with open(json_path, 'r', encoding='utf-8') as f:
        json_data = json.load(f)

    # 转换
    xml_str = convert_json_to_ids_xml(json_data, xsd_path=xsd_path, **kwargs)

    # 保存到文件
    if output_path:
        with open(output_path, 'w', encoding='utf-8') as f:
            f.write(xml_str)

    return xml_str


